<template>
  <div class="comment">
    <div class="comment-info">
      <router-link
        class="comment-avatar"
        :to="{name: 'user-id', params: {id : comment.uid}}"
      >
        <avatar :src="avatar" size="30px" ></avatar>
      </router-link>
      <div>
        <router-link class="comment-author" :to="`/user/${comment.uid}`">
          {{ comment.nickname || comment.username }}
          <span>
            {{ friendlyDate }}
          </span>
        </router-link>
        <p class="comment-content">
          <span class="wrap-open">{{ displayMessage }}</span>
        </p>
      </div>
    </div>
    <p class="comment-message" />
  </div>
</template>

<script>
import moment from 'moment'
import avatar from '@/components/avatar/index.vue'
import { isNDaysAgo } from '@/common/methods'

export default {
  name: 'CommentCard',
  components: {
    avatar
  },
  props: ['comment', 'type'],
  data() {
    return {
    }
  },
  computed: {
    displayMessage() {
      return this.comment.comment !== '' ? this.comment.comment : this.$t('p.commentNotContent')
    },
    friendlyDate() {
      const time = moment(this.comment.create_time)
      return isNDaysAgo(2, time) ? time.format('MMMDo HH:mm') : time.fromNow()
    },
    avatar() {
      if (this.comment.avatar) return this.$API.getImg(this.comment.avatar)
      return ''
    }
  }
}
</script>

